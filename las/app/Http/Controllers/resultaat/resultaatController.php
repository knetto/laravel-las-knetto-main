     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'resultaat' => 'required',
        ]);

        $resultaat = new resultaat;

        $resultaat->resultaat = $request->input('resultaat');
        
        $resultaat->student_id = $request->input('roepnaam');
        $resultaat->vak_id = $request->input('vak');



        $resultaat->save();
        return redirect('/resultaat/create')->with("success","Nieuw resultaat toegevoegd...");
    }

